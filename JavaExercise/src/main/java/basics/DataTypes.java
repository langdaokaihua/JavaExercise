package basics;

public class DataTypes {
    public static void main(String[] args) {
        byte b = 10;
        short s = 20;
        int i = 30;
        long l = 40L;

        //浮点型
        float f = 3.14F;
        double d = 6.28;

        //字符型
        char c = 'A';

        //布尔型
        boolean bool = true;
    }
}
